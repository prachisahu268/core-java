package in.com.inheritance;

public class Rectangle extends Shape{
	private int Length=0;
    private int Width= 0;
     //setterGetter
    public void setLength(int length) {
    	this.Length=length;
    }
    public int getLength() {
    	return Length;
    }
	public void setWidth(int width) {
		this.Width=width;
	}
    public int getWidth() {
    	return Width;
    }
    
	
	

}
